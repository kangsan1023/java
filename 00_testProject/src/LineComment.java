public class LineComment {
	public static void main(String[] args) {
		 // 단순한 정수 100 출력
		System.out.println(100);

		/* 인삿말과 함께
		이름을 출력 */
		System.out.println("안녕하세요!");
		System.out.println("심규홍입니다.");

		// 덧셈 결과 출력
		System.out.println(3 + 5 + 312);
	}
}
